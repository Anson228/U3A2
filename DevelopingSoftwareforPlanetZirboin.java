    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //declare variables
        double [] vrobits = new double[4];
        //Get the vtobits from user
        vrobits[0] = Double.parseDouble(getvrobits.getText());
        //Caculate the number of Drobzits
        vrobits[1] = vrobits[0] / 100000;
        vrobits[1] = Math.floor(vrobits[1]);
        Drobzit.setText(String.valueOf(vrobits[1]));
        //Caculate the number of Clickwicks
        vrobits[1] = vrobits[1] * 100000;
        vrobits[2] = vrobits[1] = vrobits[0] - vrobits[1];
        vrobits[1] = vrobits[1] / 50000;
        vrobits[1] = Math.floor(vrobits[1]);
        Clickwick.setText(String.valueOf(vrobits[1]));
        //Caculate the number of Gazoontights
        vrobits[1] = vrobits[1] * 50000;
        vrobits[2] = vrobits[1] = vrobits[2] - vrobits[1];
        vrobits[1] = vrobits[1] / 10000;
        vrobits[1] = Math.floor(vrobits[1]);
        Gazoontight.setText(String.valueOf(vrobits[1]));
        //Caculate the number of Frazoints
        vrobits[1] = vrobits[1] * 10000;
        vrobits[2] = vrobits[1] = vrobits[2] - vrobits[1];
        vrobits[1] = vrobits[1] / 1000;
        vrobits[1] = Math.floor(vrobits[1]);
        Frazoint.setText(String.valueOf(vrobits[1]));
        //Caculate the number of Blointoints
        vrobits[1] = vrobits[1] * 1000;
        vrobits[2] = vrobits[1] = vrobits[2] - vrobits[1];
        vrobits[1] = vrobits[1] / 500;
        vrobits[1] = Math.floor(vrobits[1]);
        Blointoint.setText(String.valueOf(vrobits[1]));
        //Caculate the rest of vrobits
        vrobits[1] = vrobits[2] - vrobits[1] * 500;
        rest.setText(String.valueOf(vrobits[1]));
    }//GEN-LAST:event_jButton1ActionPerformed

